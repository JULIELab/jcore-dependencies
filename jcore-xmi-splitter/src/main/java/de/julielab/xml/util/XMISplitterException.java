package de.julielab.xml.util;

public class XMISplitterException extends XMIManipulationException {
    public XMISplitterException() {
    }

    public XMISplitterException(String message) {
        super(message);
    }

    public XMISplitterException(String message, Throwable cause) {
        super(message, cause);
    }

    public XMISplitterException(Throwable cause) {
        super(cause);
    }

    public XMISplitterException(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace) {
        super(message, cause, enableSuppression, writableStackTrace);
    }
}
